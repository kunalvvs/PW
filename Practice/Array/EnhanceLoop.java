package Practice.Array;

import java.util.Scanner;

public class EnhanceLoop {
    public static void main(String[] args) {
        int a[][] = {
            {2,4,3},{3,53,4,4}
        };
        String sd[] = {"New", "Sec"};
        for( int  s[] : a)
        {
            for(int d:s)
            {
            System.out.print(d+" ");
        }
        System.out.println();

    }
    for(String f:sd)
    {
        System.out.println(f);
    }

    }
}
